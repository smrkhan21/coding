// 12. Implement a function to perform string multiplication (e.g., "2" * "3" -> "6").

function stringMultiplication(a, b) {
    return a * b;
}

function stringAddition(a, b, c) {
    return a + b + c;
}

function stringSubstraction(a, b, c) {
    return a - b - c;
}

function stringDivision(a, b) {
    return a / b;
}

function stringOperation(a, b, c, d, e) {
    return a * b + c - d / e;
}

console.log(stringMultiplication("2", "3"));
console.log(stringAddition("2", "3", "4"));
console.log(stringSubstraction("2", "3", "4"));
console.log(stringDivision("2", "3"));
console.log(stringOperation("2", "3", "4", "5", "6"));